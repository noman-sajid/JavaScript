let a = 1;
let b = 4;

console.log(a + b) //addition 
console.log(a - b) //subtraction
console.log(a * b) //multiplication
console.log(a / b) //division
console.log(a % b) //modulus
console.log(a ** b) //exponentiation
console.log(a++); //post-increment
console.log(a--); //post-decrement
console.log(++a); //pre-increment   
console.log(--a); //pre-decrement

// Assignment Operators
console.log(a == b); //equality
console.log(a != b); //inequality
console.log(a === b); //strict equality
console.log(a !== b); //strict inequality
console.log(a = b); //assignment
console.log(a += b); //addition assignment
console.log(a -= b); //subtraction assignment
console.log(a *= b); //multiplication assignment
console.log(a /= b); //division assignment
console.log(a %= b); //modulus assignment
console.log(a **= b); //exponentiation assignment
console.log(a <<= b); //left shift assignment
console.log(a >>= b); //right shift assignment
console.log(a >>>= b); //zero-fill right shift assignment

//comparison operators
console.log(a > b); //greater than
console.log(a < b); //less than
console.log(a >= b); //greater than or equal to
console.log(a <= b); //less than or equal to

// Logical Operators
console.log(a && b); //logical AND
console.log(a || b); //logical OR
console.log(!a); //logical NOT
// Bitwise Operators
console.log(a & b); //bitwise AND
console.log(a | b); //bitwise OR
console.log(a ^ b); //bitwise XOR   
console.log(~a); //bitwise NOT
console.log(a << 1); //left shift
console.log(a >> 1); //right shift
console.log(a >>> 1); //zero-fill right shift
